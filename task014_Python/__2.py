# find() # 查找子字符串,找到返回下标,否则返回-1
变量名 = "只要地球不爆炸,我相信有一天,全世界都说中国话!中国会成为全世界的风向标!"
print(变量名.find("中国")) # 找到返回先出现的下标
print(变量名.find("中国",23,30)) # 从23开始,到30结束,不包括30, 找到返回先出现的下标
print(变量名.find("美国"))
print(变量名.rfind("中国")) # 从右往左查找, 找到返回先出现的下标

