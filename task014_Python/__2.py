print("_".join(列表))

# -------------------------------------------------
# startswith() # 判断字符串是否以指定子字符串开头,返回True或False
# 语法: 字符串.startswith("子字符串",开始下标,结束下标)
# endswith() # 判断字符串是否以指定子字符串结尾,返回True或False
# 语法: 字符串.endswith("子字符串",开始下标,结束下标)
变量名 = "只要地球不爆炸,我相信有一天,全世界都说中国话!中国会成为全世界的风向标!"
print(变量名.startswith("只要",0,10)) # True
print(变量名.endswith("中国会成为全世界的风向标!",0,10)) # False
print(变量名.endswith("中国会成为全世界的风向标!")) # True
print(变量名.startswith("不要")) # False