# -------------------------------------------------
# 字符串是不可变类型 # 在修改和删除时,需注意

# 可变类型:列表,字典,集合
# 不可变类型:字符串,整数,浮点数,布尔值
# 所谓可变不可变:数据能直接修改是可变,否则是不可变
# 可变类型:变量值变化,id内存地址不变
# 不可变类型:变量值变化,id内存地址就变化了

# -------------------------------------------------
# replace() # 替换子字符串,返回新的字符串
# 语法: 字符串.replace("旧子字符串","新子字符串",替换次数),替换次数>实际,替换所有
print(变量名.replace("中国","祖国",1)) # 替换第一个"中国"为"美国"
print(id(变量名)) # 查看变量名的id内存地址
print(变量名.replace("中国","祖国")) # 替换所有的"中国"为"美国"
print(id(变量名.replace("中国","祖国"))) # 查看替换后的字符串的id内存地址, 发现id内存地址变化
print(变量名) # 发现这还是之前的字符串, 说明replace()方法是返回新的字符串, 而不是在原字符串上修改
#所以要重新赋值
新变量名 = 变量名.replace("中国","祖国",2)
print(新变量名) # 发现这已经是替换后的字符串了

# -------------------------------------------------
# split() # 分割字符串,返回分割后的列表
# 语法: 字符串.split("分隔符",分割次数)
print(变量名.split("中国")) # 以"中国"为分隔符, 分割字符串, 返回列表
print(变量名.split("中国",1)) # 以"中国"为分隔符, 分割字符串, 返回列表, 最多分割1次

最新变量名 = 变量名.split("中国") # 不要忘记赋值不可变类型
print(最新变量名) # 发现这已经是分割后的列表了

# -------------------------------------------------
# join() # 合并列表,返回新的字符串
# 语法: "分隔符".join(列表)
print("中国".join(最新变量名)) # 以"中国"为分隔符, 合并列表, 返回新的字符串
列表 = ["中","国","最","强"]
