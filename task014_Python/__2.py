# 2.切片
# 语法: 序列[开始:结束:步长]
print(e[3:6]) # 从3开始,到6结束,不包括6
print(e[1:]) # 从1开始,到最后
print(e[:5]) # 从0开始,到5结束,不包括5
print(e[:]) # 从0开始,到最后
print(e[1:10:1]) #步长默认是1,表示隔几个取
print(e[1:10:2]) # 从1开始,到10结束,不包括10, 步长是2, 表示隔一个取一个
print(e[::-1]) # 步长是-1, 表示倒序输出
print(e[::-2]) # 步长是-2, 表示倒序输出, 隔一个取一个
print(e[-4:-1]) # 最后为-1,倒着取, 从-4开始,到-1结束,不包括-1

